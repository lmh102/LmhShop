    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {

        int top = Integer.parseInt(request.getParameter("top"));
        int discount = Integer.parseInt(request.getParameter("discount"));
        ProductDAL _servirce = new ProductDAL();
        ArrayList<ProductDTO> list = _servirce.getAllProduct();
        int rate = list.get(0).getAvgRate();
        for (ProductDTO p : list) {
            if (p.getAvgRate() < rate) {
                rate = p.getAvgRate();
            }
        }
        ArrayList<Integer> listId = new ArrayList<>();
        int count = 0;
        for (ProductDTO p : list) {
            if (p.getAvgRate() == rate) {
                count++;
                listId.add(p.getProductId());
            }
            if (count == top) {
                break;
            }
        }
        _servirce.updateDiscount(listId, discount);
        response.sendRedirect("/LmhShop/admin/home");
    }

